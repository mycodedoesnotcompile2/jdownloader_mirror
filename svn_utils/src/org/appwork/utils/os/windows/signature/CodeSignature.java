package org.appwork.utils.os.windows.signature;

import java.util.Date;
import java.util.LinkedHashMap;
import java.util.Map;

/**
 * @author thomas
 * @date 13.10.2025
 *
 */
public class CodeSignature {
    private Map<String, String> issuedTo = new LinkedHashMap<String, String>();
    private Map<String, String> issuedBy = new LinkedHashMap<String, String>();
    private String              fingerprint;
    private Date                validUntil;
    private Date                signingDate;

    public Map<String, String> getIssuedTo() {
        return issuedTo;
    }

    public Map<String, String> getIssuedBy() {
        return issuedBy;
    }

    public void addIssuedToField(String key, String value) {
        this.issuedTo.put(key, value);
    }

    public void addIssuedByField(String key, String value) {
        this.issuedBy.put(key, value);
    }

    public String getFingerprint() {
        return fingerprint;
    }

    public void setFingerprint(String fingerprint) {
        this.fingerprint = fingerprint;
    }

    public Date getValidUntil() {
        return validUntil;
    }

    public void setValidUntil(Date validUntil) {
        this.validUntil = validUntil;
    }

    public Date getSigningDate() {
        return signingDate;
    }

    public void setSigningDate(Date signingDate) {
        this.signingDate = signingDate;
    }

    @Override
    public String toString() {
        return "CodeSignature{" + "issuedTo=" + issuedTo + ", issuedBy=" + issuedBy + ", fingerprint='" + fingerprint + '\'' + ", validUntil=" + validUntil + ", signingDate=" + signingDate + '}';
    }
}
